//! Unit tests for correlation ID uniqueness (F-13).
//!
//! Verifies that the ACP handshake and runtime driver generate IDs that:
//!
//! - Match the `intercom-{purpose}-{uuid}` pattern (S022, S023)
//! - Are unique across 10,000 sequential calls (S024)
//! - Are unique across concurrent sessions (S026)
//!
//! These tests FAIL before the F-13 implementation because the current code
//! uses static constants (`intercom-init-1`, `intercom-sess-1`,
//! `intercom-prompt-1`) and a global `AtomicU64` counter, which produce
//! collisions across sessions and restarts.

use agent_intercom::acp::handshake::generate_correlation_id;
use uuid::Uuid;

// ── T023 / S022 — handshake IDs match intercom-{purpose}-{uuid} pattern ──────

/// Handshake `initialize` ID must match `intercom-init-{uuid}` (S022).
#[test]
fn init_id_matches_intercom_init_uuid_pattern() {
    let id = generate_correlation_id("init");
    assert!(
        id.starts_with("intercom-init-"),
        "S022: init ID must start with 'intercom-init-', got: {id}"
    );
    let uuid_part = id
        .strip_prefix("intercom-init-")
        .expect("prefix confirmed above");
    Uuid::parse_str(uuid_part).unwrap_or_else(|e| {
        panic!("S022: suffix of init ID must be a valid UUIDv4, got '{uuid_part}': {e}")
    });
}

/// Handshake `session/new` ID must match `intercom-sess-{uuid}` (S022).
